function clicou() {
  // selecionando primeiro li que entrar 
  const li = document.querySelector('li');

  // Ao clicar no botão, vai selecionar o elemento li e trocar o background-color para azul
  li.style.backgroundColor = '#00f'
  // Aumentar o tamanho da fonte ao clicar no botão 
  li.style.fontSize = '20px';
}